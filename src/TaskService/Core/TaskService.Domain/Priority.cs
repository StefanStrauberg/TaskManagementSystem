namespace TaskService.Domain;

/// <summary>
/// Defines possible levels of task priority.
/// </summary>
public enum Priority
{
  /// <summary>
  /// Indicates that the task has a low priority level.
  /// </summary>
  Low,

  /// <summary>
  /// Indicates that the task has a medium priority level.
  /// </summary>
  Medium,

  /// <summary>
  /// Indicates that the task has a high priority level.
  /// </summary>
  High
}
